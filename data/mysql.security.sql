DROP TABLE IF EXISTS SEC_001_Aplicacion;
CREATE TABLE SEC_001_Aplicacion (
	id 		INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  type 	INT(1) NOT NULL,
  serv  VARCHAR(256) NOT NULL,
  nomb 	VARCHAR(256) NOT NULL,
  vers 	VARCHAR(16) NOT NULL,
  sope 	VARCHAR(256) NOT NULL,
  db 		VARCHAR(64) NOT NULL,
  idio 	VARCHAR(256) NOT NULL,
  obse 	VARCHAR(256) NOT NULL,
	user 	VARCHAR(30) NOT NULL
);

DROP TABLE IF EXISTS SEC_002_Api;
CREATE TABLE SEC_002_Api (
  id 		VARCHAR(256) NOT NULL,
  nomb 	VARCHAR(256) NOT NULL,
  func 	VARCHAR(256) NOT NULL,
  descr VARCHAR(256) NOT NULL,
	param VARCHAR(256) NOT NULL,
  esta 	INT(1) NOT NULL,
	idapp INT(6) UNSIGNED
);

